export class FizzBuzz {

    compute(input: number) {
        if (input === 1) {
            return '1';
        }

        if (input === 2) {
            return '2';
        }

        if (input === 3) {
            return 'Fizz';
        }

        if (input === 4) {
            return '4';
        }
    }
}